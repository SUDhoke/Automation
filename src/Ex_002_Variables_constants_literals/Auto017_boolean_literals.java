package Ex_002_Variables_constants_literals;

public class Auto017_boolean_literals
{
    public static void main(String[] args) {

        boolean b1 = true;
        boolean b2 = false;
        boolean b3 = (!b1);

        System.out.println(b3);
        System.out.println(b2);
        System.out.println(b1);
    }
}
