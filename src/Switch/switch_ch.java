package Switch;

public class switch_ch
{
    public static void main(String[] args) {
        char ch = 'p';

        switch(ch)
        {
            case 'a':
                System.out.println("print a");
                break;
            case 'b':
                System.out.println("print b");
                break;
            case 'p':
                System.out.println("print P");
                break;
            default :
                System.out.println("not known");
        }
    }
}
