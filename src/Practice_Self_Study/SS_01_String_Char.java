package Practice_Self_Study;

public class SS_01_String_Char
{
    public static void main(String[] args) {
        String name = "Sonali";

        char ch = name.charAt(0);
        char ch1 = name.charAt(1);
        char ch3 = name.charAt(2);
        char ch4 = name.charAt(3);
        char ch5 = name.charAt(4);
        char ch6 = name.charAt(5);
        System.out.println("1 :"+ch);
        System.out.println("2 :"+ch1);
        System.out.println("3 :"+ch3);
        System.out.println("4 :"+ch4);
        System.out.println("5 :"+ch5);
        System.out.println("6 :"+ch6);
    }
}
