package Variables_constants_literals;

public class Auto015_Integer_literals
{
    public static void main(String[] args) {
        //literals

        int age = 10;
         age = 66; // value of the variables/literals can be changed anytime in the prog

        System.out.println(age);

        //constant -> value of the constant is fixed during the program execution
        // final word need to use to declare constant variable

        final int age1 = 18;
        //int age1=19;
        //age1 = 19; not possible  its a constant

        System.out.println(age1);

    }
}
