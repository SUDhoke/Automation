package Ex_018_OOPs_Constructor;

public class Param_constructor
{
    String name;
    String model;
    int year ;


    //parameterised constructor
    Param_constructor(String name_c,String model_c, int age_c)
    {
        this.name = name_c;
        this.model = model_c;
        this.year = age_c;
    }
}
