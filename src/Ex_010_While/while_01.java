package Ex_010_While;

public class while_01
{
    public static void main(String[] args) {

        //first initaialization is done and then condition and then updation

        int a = 10 ; // this is initialization

        while(a < 15)///  condition
        {
            System.out.println("test -> working fine");

            a++; //updation
        }
        System.out.println("end of while loop");
    }
}
