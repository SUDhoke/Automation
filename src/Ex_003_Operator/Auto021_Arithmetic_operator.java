package Ex_003_Operator;

public class Auto021_Arithmetic_operator
{
    public static void main(String[] args) {
         // + addition
        // - substraction
        // * multiplication
        // / division
        // % modulus

        int a = 12;
        int b = 14;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        int a1 = 21;
        int b1 = 10;
        float c1 = 3f;

        System.out.println(a1/b1%c1);

    }
}
