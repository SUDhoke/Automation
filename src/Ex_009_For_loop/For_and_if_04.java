package Ex_009_For_loop;

public class For_and_if_04
{
    public static void main(String[] args) {
        for (int i = 0; i<=18; i++)
        {
            if(i >= 18 )
            {
                System.out.println("talent");
            }else{
                System.out.println("need more hardwork");
            }
        }
    }
}
